#!/bin/bash
# ==================================================
# 启动 Web 服务脚本
# 支持 Linux 和 macOS
# ==================================================

# 设置语言为中文
export LANG=zh_CN.UTF-8

echo "=================================================="
echo "启动 Web 服务..."
echo "=================================================="
echo "访问地址: http://localhost:8080"
echo "=================================================="

# 激活虚拟环境（如果存在）
if [ -d ".venv" ]; then
    echo "激活虚拟环境..."
    source .venv/bin/activate
fi

# 启动 Flask 应用
python web_app.py
